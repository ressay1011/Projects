/**
 * Print Face.
 * This class displays a face shape made of various characters in the console.
 * It demonstrates the use of multiple lines and character alignment for simple
 * ASCII art.
 */
public class PrintFace {
    /**
     * Main entry point of the program.
     * Outputs a pre-defined face pattern to the console.
     * 
     * @param args Command line arguments (not used).
     */
    public static void main(String[] args) {

        // Print a face using text characters, arranged to form a simple emoticon-style
        // face
        System.out.print(" @@@@@@@ \n@| O O |@\n(|  ^  |)\n | [_] |\n +-----+");
    }
}
