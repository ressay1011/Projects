/**
 * Print Diamond.
 * This class displays a diamond shape made of asterisks in the console.
 * It demonstrates simple string output with escape characters for formatting.
 */
public class PrintDiamond {
    /**
     * Main entry point of the program.
     * Outputs a pre-defined diamond pattern to the console.
     * 
     * @param args Command line arguments (not used).
     */
    public static void main(String[] args) {

        // Print a diamond shape using asterisks, where each line represents a row of
        // the diamond
        System.out.println("   *   \n  ***  \n ***** \n*******\n ***** \n  ***  \n   *   ");
    }
}
